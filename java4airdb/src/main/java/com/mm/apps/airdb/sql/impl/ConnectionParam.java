package com.mm.apps.airdb.sql.impl;

import com.mm.apps.airdb.sql.IConnectionParam;

public class ConnectionParam implements IConnectionParam {
	private String uRL;
	private String password;
	private String username;
	private boolean dNSConnection;
	
	
	public boolean isDNSConnection() {
		return dNSConnection;
	}
	public void setDNSConnection(boolean connection) {
		dNSConnection = connection;
	}
	public String getURL() {
		return uRL;
	}
	public void setURL(String url) {
		uRL = url;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
}
