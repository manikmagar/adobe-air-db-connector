package com.mm.apps.airdb.sql.impl;

import com.mm.apps.airdb.sql.IQuery;
import com.mm.apps.airdb.sql.ISqlParameters;
import com.mm.apps.airdb.sql.QueryType;

public class Query implements IQuery {

	private String id;
	private String sql;
	private ISqlParameters parameters;
	private QueryType type;
	
	
	public QueryType getType() {
		return type;
	}
	public void setType(QueryType type) {
		this.type = type;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSql() {
		return sql;
	}
	public void setSql(String sql) {
		this.sql = sql;
	}
	public ISqlParameters getParameters() {
		return parameters;
	}
	public void setParameters(ISqlParameters parameters) {
		this.parameters = parameters;
	}
	
}
